
public class Variaveis {

	public static void main(String[] args) {
		// mostra idade e nome
		int idade;
		String nome;
		
		nome="Marcela Goncalves Santos";
		idade=35;
		
		System.out.println("Nome: "+nome);
		System.out.println("Idade: "+idade);
		
		idade=36;
		System.out.println("Modificando variavel");
		System.out.println("Idade: "+idade);


	}

}
