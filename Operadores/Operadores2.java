
public class Operadores2 {

	public static void main(String[] args) {
		boolean A,B,resultado;
		
		A=true;
		B=false;
		
		//and logico
		resultado = A && B;
		System.out.println("A AND B : "+resultado);

		// or logico
		resultado= A || B;
		System.out.println("A OR B : "+resultado);
		
		// not
		resultado= !A;
		System.out.println("NOT A : "+resultado);


		
	}

}
