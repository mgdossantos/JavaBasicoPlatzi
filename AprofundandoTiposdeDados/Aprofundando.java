
public class Aprofundando {

	public static void main(String[] args) {
		// armazena uma letra
		char cadeira;
		cadeira='o';
		System.out.println("Qual o status da cadeira? "+cadeira);
		
		cadeira='l';
		System.out.println("Qual o status da cadeira? "+cadeira);
		
		boolean lampada;
		
		lampada=true;
		System.out.println("Qual o status da lampada? "+lampada);
		lampada=false;
		System.out.println("Qual o status da lampada? "+lampada);



	}

}
